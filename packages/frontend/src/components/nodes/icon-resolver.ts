export function resolveIcon(name: string): LucideIcon {
  return iconMap[name] || Settings;
}

export function getAvailableIcons(): { name: string; icon: LucideIcon }[] {
  return Object.entries(iconMap).map(([name, icon]) => ({ name, icon }));
}
